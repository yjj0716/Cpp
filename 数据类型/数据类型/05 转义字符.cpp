#include <iostream>
using namespace std;

int main5()
{
	//转义字符

	//换行符号 \n

	cout << "hello world\n";

	//反斜杠 \\

	cout << "\\" << endl;

	//水平制表符 \t		作用可以整齐输出数据

	cout << "aaaa\thello world" << endl;
	cout << "aa\thello world" << endl;
	cout << "aaaaaa\thello world" << endl;

	system("pause");

return 0;
}